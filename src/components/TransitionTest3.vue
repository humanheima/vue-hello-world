import gsap from 'gsap'

export default {
    data() {
        return {
            show: true
        }
    },
    methods: {
        onBeforeEnter,
        onEnter,
        onLeave
    }
}

function onBeforeEnter(el) {
    gsap.set(el, {
        scaleX: 0.25,
        scaleY: 0.25,
        opacity: 1
    })
}

function onEnter(el, done) {
    gsap.to(el, {
        duration: 1,
        scaleX: 1,
        scaleY: 1,
        opacity: 1,
        ease: 'elastic.inOut(2.5, 1)',
        onComplete: done
    })
}

function onLeave(el, done) {
    gsap.to(el, {
        duration: 0.7,
        scaleX: 1,
        scaleY: 1,
        x: 300,
        ease: 'elastic.inOut(2.5, 1)'
    })
